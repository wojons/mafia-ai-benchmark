/**
 * Get a random name from the database (unique, not yet used)
 */
let usedNames = new Set();

function getRandomName() {
  // Flatten all names into one array
  const allNames = [
    ...NAME_DATABASE.african,
    ...NAME_DATABASE.asian,
    ...NAME_DATABASE.european,
    ...NAME_DATABASE.latin,
    ...NAME_DATABASE.middleEastern,
    ...NAME_DATABASE.oceanic,
  ];

  // Filter out used names
  const availableNames = allNames.filter((name) => !usedNames.has(name));

  // If we've used all names, reset (shouldn't happen with 1000+ names and 6-10 players)
  if (availableNames.length === 0) {
    usedNames.clear();
    return allNames[Math.floor(Math.random() * allNames.length)];
  }

  const randomName =
    availableNames[Math.floor(Math.random() * availableNames.length)];
  usedNames.add(randomName);

  return randomName;
}

/**
 * Reset the used names tracker
 */
function resetUsedNames() {
  usedNames.clear();
}

/**
 * Get multiple unique names
 */
function getRandomNames(count) {
  const names = [];
  for (let i = 0; i < count; i++) {
    names.push(getRandomName());
  }
  return names;
}

